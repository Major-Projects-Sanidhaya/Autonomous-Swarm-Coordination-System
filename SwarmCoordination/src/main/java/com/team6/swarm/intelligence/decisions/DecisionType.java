/**
 * DECISIONTYPE ENUM - Type of Decision Mechanism Used
 */
public enum DecisionType {
    SIMPLE_VOTING,      // Basic majority vote
    WEIGHTED_VOTING,    // Expertise-weighted vote
    MULTI_CRITERIA,     // Multi-criteria analysis
    AUCTION,            // Auction-based allocation
    CONSENSUS,          // Consensus building
    LEADER_DECISION     // Leader makes decision
}
